#include <iostream> 
using namespace std ;
int main ()
{
    // for loop to print numbers from 0 to 10 
    for (int i = 0 ; i < 11; i++)
    {
        cout << i << endl;
    }
    // while loop 
    int j =0 ; 
    while (j < 11)
    {
         cout << j << endl;
         j ++;
    }
    //do while loop
    int k =0; 
    do {
        cout << k <<endl;
        k++;
    }while (k <  11);
    return 0;
}